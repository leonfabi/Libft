int	ft_isalpha(int c)
{
	if (c >= 'A' && c <= 'z')
		return (1);
	return (0);
}

#include <stdio.h>
int	main(void)
{
	int	c;

	c = 101;
	printf( "Ausgabe: %d", ft_isalpha(c));
	return 1;
}
